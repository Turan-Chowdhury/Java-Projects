
package foreachloop;

public class ForEachLoop {

    public static void main(String[] args) {
        
        int[] number = {2,3,4};
        
        for(int x : number)
            System.out.println(x);
        
               
        String[] names = {"turan", "asif", "fahad", "anik"};
        
        for(String x : names)
            System.out.println(x);  
    }
    
}
